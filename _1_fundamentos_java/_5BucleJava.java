package java_mini_proyectos._1_fundamentos_java;

public class _5BucleJava {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("Número for: " + i);
        }

        int b = 1;
        while (b <= 10) {
            System.out.println("Número while:" + b);
        }

        int c = 1;
        do {
            System.out.println("Número do-while: " + c);
            c++;
        } while (c <= 1);
    }

}
