package java_mini_proyectos;

public class _2Condicionales {
    public static void main(String[] args) {
        int nota = 85;

        if(nota >= 90) {
            System.out.println("Excelente");
        }else if (nota >= 70) { 
            System.out.println("Bien");
        }else if (nota >= 60) {
            System.out.println("Suficiente");
        }else {
            System.out.println("Reprobado");
        }
    }
    
}
